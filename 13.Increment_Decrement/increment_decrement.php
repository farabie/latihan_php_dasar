<?php
$a = 10;

//Post Increment
//Kembalikan $a lalu naikan 1 angka
$b = $a++;
var_dump($b);

//Pre Increment
//Naikan $a satu angka lalu kembalikan $a
$d = 12;
$c = --$d;
var_dump($c);

//Post Decrement
//Kembalikan $variable lalu turunkan 1 angka
$angka1 = 72;
$angka2 = $angka1--;
var_dump($angka2);

//Pre Decrement
//Turunkan $variable satu angka, lalu kembalikan $variable
$angka4 = 163;
$angka5 = --$angka4;
var_dump($angka5);
